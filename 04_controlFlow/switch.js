let myNumber = "hundred"

switch (myNumber) {
    case "one":
        console.log("one")
        break;
    case "ten":
        console.log("ten")
        break;        
    case "hundred":
        console.log("hundred")
        break;        
    case "thousand":
        console.log("thousand")
        break;          

    default:
        console.log("no value found");
        break;
}

//break is used to stop over compilation
